  // ─── 自定义词典管理 ───

  /**
   * 获取当前词典总大小（内置+自定义）
   */
  getDictionarySize(): number {
    return this.builtinDictionary.length + this.customDictionary.length;
  }

  /**
   * 获取所有词典条目
   */
  getAllEntries(): TypoEntry[] {
    return [...this.builtinDictionary, ...this.customDictionary];
  }

  /**
   * 获取用户自定义条目
   */
  getCustomEntries(): TypoEntry[] {
    return [...this.customDictionary];
  }

  /**
   * 添加到自定义词典
   */
  addToDictionary(wrong: string, correct: string, type: 'typo' | 'grammar' | 'mixed', description: string): TypoEntry {
    // 检查是否已存在
    const exists = this.customDictionary.some(e => e.wrong === wrong);
    if (exists) throw new Error(`词条"${wrong}"已在自定义词典中`);

    const entry: TypoEntry = { wrong, correct, type, description };
    this.customDictionary.push(entry);
    this.saveCustomDictionary();
    return entry;
  }

  /**
   * 从自定义词典移除
   */
  removeFromDictionary(wrong: string): boolean {
    const index = this.customDictionary.findIndex(e => e.wrong === wrong);
    if (index === -1) return false;
    this.customDictionary.splice(index, 1);
    this.saveCustomDictionary();
    return true;
  }

  /**
   * 清空自定义词典
   */
  clearCustomDictionary(): void {
    this.customDictionary = [];
    this.saveCustomDictionary();
  }

  // ─── 持久化 ───

  private loadCustomDictionary(): void {
    try {
      if (fs.existsSync(this.customDictPath)) {
        const raw = fs.readFileSync(this.customDictPath, 'utf-8');
        const store: CustomDictionaryStore = JSON.parse(raw);
        this.customDictionary = store.entries || [];
      }
    } catch {
      this.customDictionary = [];
    }
  }

  private saveCustomDictionary(): void {
    try {
      const dir = path.dirname(this.customDictPath);
      if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
      const store: CustomDictionaryStore = {
        entries: this.customDictionary,
        version: '1.0',
        updatedAt: new Date().toISOString(),
      };
      fs.writeFileSync(this.customDictPath, JSON.stringify(store, null, 2), 'utf-8');
    } catch (err) {
      console.error('保存自定义词典失败:', err);
    }
  }

  // ─── 检测与修正 ───

  /**
   * 实时检测内容中的错别字（内置+自定义词典）
   */
  check(content: string, mode: string = 'all'): SpellError[] {
    const errors: SpellError[] = [];
    const seen = new Set<string>();
    const allEntries = [...this.builtinDictionary, ...this.customDictionary];

    for (const entry of allEntries) {
      if (mode !== 'all' && entry.type !== mode) continue;

      const regex = new RegExp(entry.wrong.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi');
      let match: RegExpExecArray | null;
      while ((match = regex.exec(content)) !== null) {
        const key = `${match.index}-${entry.wrong}`;
        if (seen.has(key)) continue;
        seen.add(key);

        const start = Math.max(0, match.index - 10);
        const context = (start > 0 ? '...' : '') + content.slice(start, match.index + entry.wrong.length + 10) + '...';

        errors.push({
          index: match.index,
          word: match[0],
          suggestion: entry.correct,
          type: entry.type,
          context,
        });
      }
    }

    return errors;
  }

  /**
   * 批量修正
   */
  batchFix(content: string, fixes: { index: number; replacement: string }[]): string {
    let result = content;
    const sortedFixes = [...fixes].sort((a, b) => b.index - a.index);

    for (const fix of sortedFixes) {
      const entry = this.findEntryAtIndex(result, fix.index);
      if (entry) {
        result = result.slice(0, fix.index) + fix.replacement + result.slice(fix.index + entry.wrong.length);
      }
    }

    return result;
  }

  /**
   * 一键修正所有错误
   */
  autoFix(content: string): { result: string; fixes: { word: string; replacement: string; index: number }[] } {
    let result = content;
    const fixes: { word: string; replacement: string; index: number }[] = [];
    const allErrors = this.check(content, 'all');
    const sortedErrors = [...allErrors].sort((a, b) => b.index - a.index);

    for (const error of sortedErrors) {
      result = result.slice(0, error.index) + error.suggestion + result.slice(error.index + error.word.length);
      fixes.push({ word: error.word, replacement: error.suggestion, index: error.index });
    }

    return { result, fixes };
  }

  private findEntryAtIndex(content: string, index: number): TypoEntry | undefined {
    const remaining = content.slice(index);
    return this.getAllEntries().find((entry) => remaining.startsWith(entry.wrong));
  }
